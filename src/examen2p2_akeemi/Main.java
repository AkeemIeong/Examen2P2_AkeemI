            componentes.add(new Teclado(a, b, c));
            Teclado p=new Teclado(a, b, c);
            adminPieza ap = new adminPieza("./parte.segs");
            ap.cargararchivo();
            ap.setParte(p);
            ap.escribirArchivo();
